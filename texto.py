titulo_tendencia = '¿Cómo influye a la población una tasa de natalidad baja?'
texto_tendencia = '''
Una tasa de natalidad baja forma una pirámide de población con una forma más o menos invertida. 
La mayor parte de la población se concentra en las zonas intermedias o superiores de la pirámide. 
Este tipo de pirámides de población es típica de países desarrollados o del primer mundo, 
en los que la tasa de natalidad es bastante baja y la población está envejecida.
'''

titulo_poblacion = '¿Qué efectos causa una población envejecida'
texto_poblacion = '''
Disminuye el crecimiento global de un país por efecto de reducción de la demanda y cambio 
de la misma, aumentando los gastos en salud por el incremento de la población de mayor edad. 
Por lo tanto cuando se reduce la natalidad se estanca el crecimiento económico y el progreso. 
'''
